using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _05_Math
{
    internal class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Max: " + Math.Max(5, 15));
            Console.WriteLine("Min: " + Math.Min(5, 15));
            Console.WriteLine("Square Root: " + Math.Sqrt(25));
            Console.WriteLine("Rounded: " + Math.Round(9.78));
            Console.ReadLine();
        }
    }
}
